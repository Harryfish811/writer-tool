# 需要特殊检测的常见错误模式（正则）
TYPO_PATTERNS = [
    (r"的地得([^的地得])", r"的\g<1>"),      # 的/地/得混淆简化
    (r"([^的地得])的得", r"\g<1>的"),          # 逆向检测
    (r"象([\u4e00-\u9fa5]{1,3}?)像", r"像\g<1>像"),  # 象/像混淆
    (r"做([\u4e00-\u9fa5]{1,3}?)作", r"做\g<1>作"),  # 做/作混淆
    (r"再([\u4e00-\u9fa5]{1,2})见", r"再\g<1>见"), # 再/在混淆
    (r"([\u4e00-\u9fa5])己([\u4e00-\u9fa5]{0,2})经", r"\g<1>已\g<2>经"),  # 己/已混淆
]

# 用户自定义词库缓存
_CUSTOM_MAP: dict = {}


def load_custom_dict(path: str) -> int:
    """
    从文件加载自定义错别字词库
    格式：每行 `错误 正确`（空格/Tab 分隔），# 开头为注释
    返回加载条数
    """
    global _CUSTOM_MAP
    count = 0
    if not os.path.exists(path):
        return 0
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            parts = line.split()
            if len(parts) >= 2:
                _CUSTOM_MAP[parts[0]] = parts[1]
                count += 1
    return count


def _merge_map() -> dict:
    """合并内置词库与自定义词库（自定义优先）"""
    merged = dict(TYPO_MAP)
    merged.update(_CUSTOM_MAP)
    return merged


def check_typo(text: str) -> list:
    """
    检测文本中的错别字
    返回: [{'type': 'typo', 'original': 'xxx', 'correct': 'xxx',
            'position': int, 'reason': '...'}, ...]
    """
    issues = []
    typo_map = _merge_map()

    # 1. 词组级别检测（遍历错别字词库）
    for wrong, correct in typo_map.items():
        if wrong == correct or not wrong:
            continue
        start = 0
        while True:
            idx = text.find(wrong, start)
            if idx == -1:
                break
            issues.append({
                "type": "typo",
                "subtype": "word",
                "original": wrong,
                "correct": correct,
                "position": idx,
                "reason": f"常见错别字：「{wrong}」→「{correct}」"
            })
            start = idx + 1

    # 2. 模式级别检测（正则）
    for pattern, replacement in TYPO_PATTERNS:
        for match in re.finditer(pattern, text):
            original = match.group()
            fixed = re.sub(pattern, replacement, original)
            if fixed != original:
                issues.append({
                    "type": "typo",
                    "subtype": "pattern",
                    "original": original,
                    "correct": fixed,
                    "position": match.start(),
                    "reason": "疑似用词错误（词性混淆）"
                })

    # 去重（同一位置只保留一个，取最长的）
    issues.sort(key=lambda x: (-x["position"], -len(x["original"])))
    filtered = []
    seen_pos = set()
    for issue in issues:
        pos = issue["position"]
        if pos not in seen_pos:
            filtered.append(issue)
            seen_pos.add(pos)
    filtered.sort(key=lambda x: x["position"])

    return filtered


def highlight_issues(text: str, issues: list) -> str:
    """把检测结果高亮标注出来"""
    result = text
    offset = 0
    for issue in issues:
        pos = issue["position"] + offset
        orig_len = len(issue["original"])
        correct = issue["correct"]
        tag = f"[❌{correct}]"
        result = result[:pos] + tag + result[pos + orig_len:]
        offset += len(tag) - orig_len
    return result


def apply_fixes(text: str, issues: list = None) -> str:
    """应用所有修正，返回修正后的文本"""
    if issues is None:
        issues = check_typo(text)
    if not issues:
        return text
    result = text
    offset = 0
    for issue in sorted(issues, key=lambda x: x["position"]):
        pos = issue["position"] + offset
        orig_len = len(issue["original"])
        result = result[:pos] + issue["correct"] + result[pos + orig_len:]
        offset += len(issue["correct"]) - orig_len
    return result


def __check_integrity() -> None:
    """
    模块加载时校验词库完整性：
    - 不允许 错误==正确（自环）
    - 不允许 空键
    反向（正确→错误）无法静态识别，交由测试覆盖。
    """
    bad = [k for k, v in TYPO_MAP.items() if k == v or not k.strip()]
    if bad:
        raise RuntimeError(f"TYPO_MAP 存在非法条目（自环/空键）：{bad[:10]}")


__check_integrity()
